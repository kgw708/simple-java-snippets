package snippets.math;

public class FourArithmeticOperations {
    public static void main(String[] args) {
        int a = 100;
        int b = 25;
        int c = 30;

        // 加算
        System.out.println(a + b);

        // 減算
        System.out.println(a - b);

        // 乗算
        System.out.println(a * b);

        // 除算
        System.out.println(a / b);

        // 除算の商と余り
        System.out.println(a / c);  // 商
        System.out.println(a % c);  // 余り
    }
}
